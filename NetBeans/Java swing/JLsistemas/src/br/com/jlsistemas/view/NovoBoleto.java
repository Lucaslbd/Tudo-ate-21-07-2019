    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jDateChooser1.getDate() == null) {
            JOptionPane.showMessageDialog(rootPane, "Preencha a data de vencimento");
        } else {
            carregar.setVisible(true);
            Thread t = new Thread() {
                @Override
                public void run() {
                    Boleto boleto = new Boleto();
                    boleto.setTotaParcelas(Integer.parseInt((String) jComboBoxParcelas.getSelectedItem()));
                    boleto.setEmpresa((Empresa) jComboBoxEmpresa.getSelectedItem());
                    boleto.setVencimento(jDateChooser1.getDate());
                    BoletoDao dao = new BoletoDao();
                    dao.NovaCobranca(boleto);
                    dispose();
                    carregar.dispose();
                }
            };
            t.start();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

