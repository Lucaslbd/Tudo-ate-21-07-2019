    private void ConfirmarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ConfirmarKeyPressed
         if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(TXTCodigo.getText().isEmpty()){
         JOptionPane.showMessageDialog(null,"Insira o codigo da venda");
        TXTCodigo.requestFocus(); 
          }
          String codigo = TXTCodigo.getText();
        conex.Conexao();
         try {
               conex.executaSql("select * from caixa1 where cod_venda='"+codigo+"'");
               conex.rs.first();
               jTextFieldCodVenda.setText(conex.rs.getString("nome_produto"));
               mod.setCodVenda(Integer.parseInt(TXTCodigo.getText()));
             control.Excluir(mod);
             ExecutaMetodo();
             TXTCodigo.setText("");
             jTextFieldCodVenda.setText("");
             dispose();
              } catch  (SQLException ex) {
               JOptionPane.showMessageDialog(null,"Este codigo n√£o esta na lista");          
                 TXTCodigo.requestFocus();          
              } 
           
         }
    }//GEN-LAST:event_ConfirmarKeyPressed

