    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int escolha = JOptionPane.showConfirmDialog(rootPane, "Sim ou Não");
        switch (escolha) {
            case 0:
                JOptionPane.showMessageDialog(this, "Sim", "Informação", JOptionPane.INFORMATION_MESSAGE);
                break;
            case 1:
                JOptionPane.showMessageDialog(this, "Não", "Informação", JOptionPane.INFORMATION_MESSAGE);
                break;
            case 2:
                JOptionPane.showMessageDialog(this, "Cancelou", "Informação", JOptionPane.INFORMATION_MESSAGE);
                break;
            default:
                JOptionPane.showMessageDialog(this, "Apertou no x", "Informação", JOptionPane.INFORMATION_MESSAGE);
                break;
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String[] opcoes = {"Dinheiro", "Credito", "Debito", "Crediário"};
        String escolha = String.valueOf(JOptionPane.showInputDialog(null, "Escolha a forma de pagamento", "Escolha", JOptionPane.QUESTION_MESSAGE, null, opcoes, opcoes[0]));
        if (escolha.equals("null")){
            JOptionPane.showMessageDialog(this, "Você cancelou");
        } else {
            JOptionPane.showMessageDialog(this, escolha);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

