    public void preencherTabela(String sql){
        ArrayList dados = new ArrayList();
        String [] colunas = new String []{"ID","Produto","cheios","vazios","Total"};
        conex.Conexao();
        conex.executaSql(sql);
           try {
               conex.rs.first();
               do{
                 dados.add(new Object[]{conex.rs.getString("cod_produto"),conex.rs.getString("nome_produto"),conex.rs.getString("cheio"),conex.rs.getString("vazio"),(conex.rs.getInt("cheio")+conex.rs.getInt("vazio"))});
                }while(conex.rs.next());
           } catch (SQLException ex) {
               
           }
           
           ModeloTabela modelo = new ModeloTabela(dados, colunas);
           jTableProdutos.setModel(modelo);
           jTableProdutos.getColumnModel ().getColumn(0).setPreferredWidth(44);
           jTableProdutos.getColumnModel().getColumn(0).setResizable(false);
           jTableProdutos.getColumnModel ().getColumn(1).setPreferredWidth(184);
           jTableProdutos.getColumnModel().getColumn(1).setResizable(false);
           jTableProdutos.getColumnModel ().getColumn(2).setPreferredWidth(84);
           jTableProdutos.getColumnModel().getColumn(2).setResizable(false);
           jTableProdutos.getColumnModel ().getColumn(3).setPreferredWidth(84);
           jTableProdutos.getColumnModel().getColumn(3).setResizable(false);
           jTableProdutos.getColumnModel ().getColumn(4).setPreferredWidth(94);
           jTableProdutos.getColumnModel().getColumn(4).setResizable(false);
           jTableProdutos.getTableHeader().setReorderingAllowed(false);
           jTableProdutos.setAutoResizeMode(jTableProdutos.AUTO_RESIZE_OFF);
           jTableProdutos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
          
        conex.Desconecta();
    }
