    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "O campo valor não pode estar vazio");
            jTextField2.requestFocus();
        } else {
            Calendar data = Calendar.getInstance();
            Date d = data.getTime();
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
            dateFormat.format(d);
            String dtHoje;
            dtHoje = dateFormat.format(d);
            ControleFolhaPagamento folha = new ControleFolhaPagamento();
            folha.setData(dtHoje);
            folha.setDescricao((String) jComboBox1.getSelectedItem());
            folha.setValor(Double.parseDouble(jTextField2.getText()));
            folha.setFuncionario(funcionario);
            FuncionarioDao dao = new FuncionarioDao();
            dao.RegistrarPagamento(folha);
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton1MouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseEntered
         try {
            Double.parseDouble(jTextField2.getText());
        } catch (NumberFormatException e) {
            if (!"".equals(jTextField2.getText())) {
                JOptionPane.showMessageDialog(null, "O campo valor somente pode receber numeros reais ex: 50.31");
                jTextField2.setText("");
                jTextField2.requestFocus();
            }
        }
    }//GEN-LAST:event_jButton1MouseEntered

