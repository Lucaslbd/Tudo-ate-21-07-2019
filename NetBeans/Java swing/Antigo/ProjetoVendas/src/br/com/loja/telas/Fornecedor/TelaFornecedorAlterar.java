    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextFieldNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "O campo nome não pode estar vazio");
            jTextFieldNome.requestFocus();
        } else if (jFormattedTextFieldCnpj.getText().trim().length() < 18) {
            JOptionPane.showMessageDialog(rootPane, "O campo cnpj não pode estar vazio");
            jFormattedTextFieldCnpj.requestFocus();
        } else if (jFormattedTextFieldTelefone.getText().trim().length() < 12) {
            JOptionPane.showMessageDialog(rootPane, "O campo telefone não pode estar vazio");
            jFormattedTextFieldTelefone.requestFocus();
        } else if (jTextFieldEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "O campo email não pode estar vazio");
            jTextFieldEmail.requestFocus();
        } else {
            Fornecedor fornecedor = new Fornecedor();
            FornecedorDao dao = new FornecedorDao();
            fornecedor.setNome(jTextFieldNome.getText());
            fornecedor.setCnpj(jFormattedTextFieldCnpj.getText());
            fornecedor.setTelefone(jFormattedTextFieldTelefone.getText());
            fornecedor.setEmail(jTextFieldEmail.getText());
            fornecedor.setCodigo(Integer.parseInt(jLabelCodigo.getText()));
            dao.Alterar(fornecedor);
            dispose();

        }
    }//GEN-LAST:event_jButton1ActionPerformed

