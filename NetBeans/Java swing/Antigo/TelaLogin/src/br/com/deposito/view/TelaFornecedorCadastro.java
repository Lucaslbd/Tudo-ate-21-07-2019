    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextFieldNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha o campo nome", "Aviso", JOptionPane.WARNING_MESSAGE);
            jTextFieldNome.requestFocus();
        } else if (jTextFieldCnpj.getText().trim().length() < 18) {
            JOptionPane.showMessageDialog(this, "Preencha o campo cnpj", "Aviso", JOptionPane.WARNING_MESSAGE);
            jTextFieldCnpj.requestFocus();
        } else if (jTextFieldContato.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha o campo contato", "Aviso", JOptionPane.WARNING_MESSAGE);
            jTextFieldContato.requestFocus();
        } else if (jTextFieldCidade.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha o campo cidade", "Aviso", JOptionPane.WARNING_MESSAGE);
            jTextFieldCidade.requestFocus();
        } else {
            Fornecedor fornecedor = new Fornecedor();            
            fornecedor.setCidade(jTextFieldCidade.getText());
            fornecedor.setCodigo(codigo);  
            fornecedor.setCnpj(jTextFieldCnpj.getText());            
            fornecedor.setNome(jTextFieldNome.getText());            
            fornecedor.setContato(jTextFieldContato.getText());         
            FornecedorDao dao = new FornecedorDao();
            if (codigo == 0) {
                dao.salvar(fornecedor);
            } else {
                dao.alterar(fornecedor);
            }
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

