    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        JFileChooser arquivo = new JFileChooser();
        FileNameExtensionFilter filtroPDF = new FileNameExtensionFilter("Arquivos PDF,PNG,JPG,GIF,DOC,DOCX,TXT", "pdf", "png", "jpg", "gif", "docx", "doc", "txt");
        arquivo.addChoosableFileFilter(filtroPDF);
        arquivo.setAcceptAllFileFilterUsed(false);
        if (arquivo.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            jTextField2.setText(arquivo.getSelectedFile().getAbsolutePath());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha o campo assunto", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else if (jTextArea1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha o campo mensagem", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                MultiPartEmail email = new MultiPartEmail();
                email.setHostName("smtp.gmail.com");
                email.setSslSmtpPort("465");
                email.setStartTLSRequired(true);
                email.setStartTLSEnabled(true);
                email.setSSLOnConnect(true);
                email.setAuthenticator(new DefaultAuthenticator(TelaPrincipal.usuario.getEmail(), TelaPrincipal.usuario.getSenha()));
                email.setFrom(jLabel5.getText());
                email.setSubject(jTextField1.getText());
                email.setMsg(jTextArea1.getText());
                for (int i = 0; i < Modelo.getSize(); i++) {
                    email.addTo(Modelo.getElementAt(i).toString());
                }
                if (!jTextField2.getText().isEmpty()) {
                    EmailAttachment arquivo = new EmailAttachment();
                    arquivo.setPath(jTextField2.getText());
                    arquivo.setDisposition(EmailAttachment.ATTACHMENT);
                    email.attach(arquivo);
                }
                email.send();
                JOptionPane.showMessageDialog(rootPane, "Email enviado com sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            } catch (EmailException ex) {
                JOptionPane.showMessageDialog(rootPane, "Erro ao enviar email" + ex, "Erro", JOptionPane.ERROR_MESSAGE);
            }
            dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

