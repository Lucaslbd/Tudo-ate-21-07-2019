    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextFieldNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha o campo descrição do estoque", "Aviso", JOptionPane.WARNING_MESSAGE);
            jTextFieldNome.requestFocus();
        } else if (jTextFieldVazios.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha o campo vazios", "Aviso", JOptionPane.WARNING_MESSAGE);
            jTextFieldVazios.requestFocus();
        } else if (jTextFieldCustoCasco.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha o campo custo do casco", "Aviso", JOptionPane.WARNING_MESSAGE);
            jTextFieldCustoCasco.requestFocus();
        } else {
            Estoque estoque = new Estoque();
            estoque.setCodigo(codigo);
            estoque.setVazio(Integer.parseInt(jTextFieldVazios.getText()));
            estoque.setNome(jTextFieldNome.getText());
            estoque.setCustoCasco(Double.parseDouble(jTextFieldCustoCasco.getText()));
            EstoqueDao dao = new EstoqueDao();
            if (codigo == 0) {
                dao.salvar(estoque);
            } else {
                dao.alterar(estoque);
            }
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

