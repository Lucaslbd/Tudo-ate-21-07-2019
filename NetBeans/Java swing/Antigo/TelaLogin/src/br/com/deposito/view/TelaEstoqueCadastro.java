    private void jButton1MouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseEntered
        jButton1.setBackground(new Color(58, 100, 100));
        try {
            Double.parseDouble(jTextFieldCustoCasco.getText());
        } catch (NumberFormatException e) {
            if (!"".equals(jTextFieldCustoCasco.getText())) {
                JOptionPane.showMessageDialog(null, "O campo custo do casco somente pode receber numeros reais ex: 50.31", "Aviso", JOptionPane.WARNING_MESSAGE);
                jTextFieldCustoCasco.setText("");
                jTextFieldCustoCasco.requestFocus();
            }
        }
        
        try {
            Integer.parseInt(jTextFieldVazios.getText());
        } catch (NumberFormatException e) {
            if (!"".equals(jTextFieldVazios.getText())) {
                JOptionPane.showMessageDialog(null, "O campo produto vazio somente pode receber numeros inteiros ex: 50", "Aviso", JOptionPane.WARNING_MESSAGE);
                jTextFieldVazios.setText("");
                jTextFieldVazios.requestFocus();
            }
        }
    }//GEN-LAST:event_jButton1MouseEntered

