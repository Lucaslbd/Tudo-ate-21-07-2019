    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (jTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Preencha o campo nome", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else if (jDateChooser1.getDate() == null) {
            JOptionPane.showMessageDialog(rootPane, "Preencha o campo data", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else {
            usuario.setNascimento(jDateChooser1.getDate());
            usuario.setNome(jTextField2.getText());
            usuario.setEndereco((Endereco) jComboBox1.getSelectedItem());
            if (flag == 1) {
                dao.salvar(usuario);
            } else {
                dao.alterar(usuario);
            }
            jTextField2.setText("");
            jTextField2.setEnabled(false);
            jDateChooser1.setDate(null);
            jDateChooser1.setEnabled(false);
            listar();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTablePadrao.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(rootPane, "Selecione um usuário", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else {
            jTextField2.setEnabled(true);
            jDateChooser1.setEnabled(true);
            usuario = lista.get(jTablePadrao.getSelectedRow());
            jTextField2.setText(usuario.getNome());
            jDateChooser1.setDate(usuario.getNascimento());
            flag = 2;
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTablePadrao.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(rootPane, "Selecione um usuário", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else {
            usuario = lista.get(jTablePadrao.getSelectedRow());
            dao.excluir(usuario);
            listar();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void listar() {
        lista = dao.listar("Usuario.listar");
        preencherTabela(lista);
    }

    private void preencherTabela(List<Usuario> ls) {
        String[] colunas = new String[]{"Nome", "Nascimento","Endereço"};
        ArrayList dados = new ArrayList();
        if (ls == null || ls.size() < 1) {
            dados.add(new Object[]{"Nenhum registro encontrado", "",""});
        } else {
            for (Usuario c : ls) {
                dados.add(new Object[]{c.getNome(), c.getNascimento(),c.getEndereco().getLogradouro()});
            }
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jTablePadrao.setModel(modelo);
        jTablePadrao.getColumnModel().getColumn(0).setPreferredWidth(150);
        jTablePadrao.getColumnModel().getColumn(0).setResizable(false);
        jTablePadrao.getColumnModel().getColumn(1).setPreferredWidth(200);
        jTablePadrao.getColumnModel().getColumn(1).setResizable(false);
        jTablePadrao.getColumnModel().getColumn(2).setPreferredWidth(200);
        jTablePadrao.getColumnModel().getColumn(2).setResizable(false);
        jTablePadrao.getTableHeader().setReorderingAllowed(false);
        jTablePadrao.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        jTablePadrao.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

