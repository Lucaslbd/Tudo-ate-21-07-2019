    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextFieldNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "O campo nome não pode estar vazio");
            jTextFieldNome.requestFocus();
        } else if (jTextFieldCpf.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "O campo cpf não pode estar vazio");
            jTextFieldCpf.requestFocus();
        } else if (jTextFieldCargo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "O campo cargo não pode estar vazio");
            jTextFieldCargo.requestFocus();
        } else if (jTextFieldSalario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "O campo salário não pode estar vazio");
            jTextFieldSalario.requestFocus();
        } else {
            FuncionarioDao dao = new FuncionarioDao();
            Funcionario funcionario = new Funcionario();
            funcionario.setCpf(jTextFieldCpf.getText());
            funcionario.setNome(jTextFieldNome.getText());
            funcionario.setCargo(jTextFieldCargo.getText());
            funcionario.setSalario(Double.parseDouble(jTextFieldSalario.getText()));
            funcionario.setSituacao((String) jComboBox1.getSelectedItem());
            funcionario.setCodigo(codigo);
            dao.Alterar(funcionario);
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton1MouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseEntered
        try {
            Double.parseDouble(jTextFieldSalario.getText());
        } catch (NumberFormatException e) {
            if (!"".equals(jTextFieldSalario.getText())) {
                JOptionPane.showMessageDialog(null, "O campo salalário somente pode receber numeros reais ex: 50.50");
                jTextFieldSalario.setText("");
                jTextFieldSalario.requestFocus();
            }
        }
    }//GEN-LAST:event_jButton1MouseEntered

    private void preencherCampos(int codigo) {
        FuncionarioDao dao = new FuncionarioDao();
        Funcionario funcionario = dao.buscarCodigo(codigo);
        jTextFieldNome.setText(funcionario.getNome());
        jTextFieldCpf.setText(funcionario.getCpf());
        jTextFieldCargo.setText(funcionario.getCargo());
        jTextFieldSalario.setText(String.valueOf(funcionario.getSalario()));
        jComboBox1.setSelectedItem(funcionario.getSituacao());
    }

