    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jPasswordField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Preencha o campo senha");
        } else {
            carregar = true;
            UsuarioDao dao = new UsuarioDao();
            Usuario usuario = dao.autenticar(jFormattedTextField1.getText(), jPasswordField1.getText());
            if (usuario == null) {
                JOptionPane.showMessageDialog(rootPane, "CPF ou senha invalidos");
            } else if (usuario.getDeposito().getSituacao().equals("Irregular")) {
                JOptionPane.showMessageDialog(rootPane, "Entre em contato com o empresa administradora para regularizar seu plano");
            } else {
                FechamentoCaixaDao f = new FechamentoCaixaDao();
                f.fechamento(usuario.getDeposito());
                Principal tela = new Principal(usuario);
                tela.setVisible(true);
                tela.setExtendedState(MAXIMIZED_BOTH);
                dispose();
            }
            carregar = false;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

