    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextFieldPlaca.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "O campo placa n達o pode estar vazio");
            jTextFieldPlaca.requestFocus();
        } else if (jTextFieldModelo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "O campo modelo n達o pode estar vazio");
            jTextFieldModelo.requestFocus();
        } else if (jTextFieldMarca.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "O campo marca n達o pode estar vazio");
            jTextFieldMarca.requestFocus();
        } else {
            VeiculoDao dao = new VeiculoDao();
            Veiculos veiculo = new Veiculos();
            veiculo.setDeposito(deposito);
            veiculo.setModelo(jTextFieldModelo.getText());
            veiculo.setPlaca(jTextFieldPlaca.getText());
            veiculo.setMarca(jTextFieldMarca.getText());
            veiculo.setCategoria((String) jComboBox1.getSelectedItem());
            dao.Salvar(veiculo);
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

