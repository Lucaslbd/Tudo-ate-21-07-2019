    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextFieldTaxa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "O campo taxa não pode estar vazio");
            jTextFieldTaxa.requestFocus();        
        } else {
            FormaPagamentoDao dao = new FormaPagamentoDao();
            FormaPagamento pagamento = new FormaPagamento();
            pagamento.setDeposito(deposito);
            pagamento.setDesconto(Double.parseDouble(jTextFieldTaxa.getText()));
            pagamento.setTipo((String) jComboBox1.getSelectedItem());
            pagamento.setAcrescimo(Double.parseDouble(jTextFieldAcrescimo.getText()));
            dao.Salvar(pagamento);
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton1MouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseEntered
     try {
            Double.parseDouble(jTextFieldTaxa.getText());
        } catch (NumberFormatException e) {
            if (!"".equals(jTextFieldTaxa.getText())) {
                JOptionPane.showMessageDialog(null, "O campo taxa somente pode receber numeros reais ex: 50.50");
                jTextFieldTaxa.setText("");
                jTextFieldTaxa.requestFocus();
            }
        }
     
      try {
            Double.parseDouble(jTextFieldAcrescimo.getText());
        } catch (NumberFormatException e) {
            if (!"".equals(jTextFieldAcrescimo.getText())) {
                JOptionPane.showMessageDialog(null, "O campo acréscimo somente pode receber numeros reais ex: 50.50");
                jTextFieldAcrescimo.setText("");
                jTextFieldAcrescimo.requestFocus();
            }
        }
    }//GEN-LAST:event_jButton1MouseEntered

