    private void Pesquisar() {
        String pesquisa = null;
        if (jComboBoxTipoPesquisa.getSelectedItem().equals("Motivo")) {
            pesquisa = "descricao";
        } else if (jComboBoxTipoPesquisa.getSelectedItem().equals("Produto")) {
            pesquisa = "nome_produto";
        }
        jTextFieldPesquisa.setText(jList1.getSelectedValue());
        if (jDateChooser1.getDate() == null || jDateChooser2.getDate() == null) {
            preencherTabela("select * from baixa inner join produto inner join fornecedor inner join empresa on empresa = codigo_empresa on fornecedor = codigo_fornecedor on produto = codigo_produto where empresa = " + usuario.getDeposito().getCodigo() + " and " + pesquisa + " ilike'%" + jTextFieldPesquisa.getText() + "%' order by data");
        } else {
            preencherTabela("select * from baixa inner join produto inner join fornecedor inner join empresa on empresa = codigo_empresa on fornecedor = codigo_fornecedor on produto = codigo_produto where data between '" + jDateChooser1.getDate() + "' and '" + jDateChooser2.getDate() + "' and empresa = " + usuario.getDeposito().getCodigo() + " and " + pesquisa + " ilike'%" + jTextFieldPesquisa.getText() + "%' order by data");
        }
        jList1.setVisible(false);
        pesquisou = true;
    }

    private void preencherTabela(String sql) {
        UtilitarioDao util = new UtilitarioDao();
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"Codigo", "Data", "Motivo", "produto", "Qtd", "Casco", "Valor"};
        try {
            Connection conexao;
            conexao = ConexaoBD.conectar();
            PreparedStatement comando = conexao.prepareStatement(sql);
            ResultSet resultado = comando.executeQuery();
            while (resultado.next()) {
                dados.add(new Object[]{resultado.getInt("codigo_baixa"), util.ArrrumaData(resultado.getDate("data")), resultado.getString("descricao"), resultado.getString("nome_produto"), resultado.getInt("quantidade"), resultado.getString("casco"), util.ArrumaDouble(resultado.getDouble("valor") * resultado.getInt("quantidade"))});
            }
            conexao.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Baixa não encontrada");
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jTablePadrao.setModel(modelo);
        jTablePadrao.getColumnModel().getColumn(0).setPreferredWidth(70);
        jTablePadrao.getColumnModel().getColumn(0).setResizable(false);
        jTablePadrao.getColumnModel().getColumn(1).setPreferredWidth(118);
        jTablePadrao.getColumnModel().getColumn(1).setResizable(false);
        jTablePadrao.getColumnModel().getColumn(2).setPreferredWidth(180);
        jTablePadrao.getColumnModel().getColumn(2).setResizable(false);
        jTablePadrao.getColumnModel().getColumn(3).setPreferredWidth(195);
        jTablePadrao.getColumnModel().getColumn(3).setResizable(false);
        jTablePadrao.getColumnModel().getColumn(4).setPreferredWidth(90);
        jTablePadrao.getColumnModel().getColumn(4).setResizable(false);
        jTablePadrao.getColumnModel().getColumn(5).setPreferredWidth(105);
        jTablePadrao.getColumnModel().getColumn(5).setResizable(false);
        jTablePadrao.getColumnModel().getColumn(6).setPreferredWidth(70);
        jTablePadrao.getColumnModel().getColumn(6).setResizable(false);
        jTablePadrao.getTableHeader().setReorderingAllowed(false);
        jTablePadrao.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        jTablePadrao.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

    private void PreencherLista() {
        String pesquisa = null;
        if (jComboBoxTipoPesquisa.getSelectedItem().equals("Motivo")) {
            pesquisa = "descricao";
        } else if (jComboBoxTipoPesquisa.getSelectedItem().equals("Produto")) {
            pesquisa = "nome_produto";
        }
        if (jTextFieldPesquisa.getText().isEmpty()) {
            jList1.removeAll();
            Modelo.removeAllElements();
            jList1.setVisible(false);
        } else {
            jList1.setVisible(true);
            StringBuilder sql = new StringBuilder();
            sql.append("select DISTINCT ").append(pesquisa).append(" from baixa inner join produto inner join fornecedor inner join empresa on empresa = codigo_empresa on fornecedor = codigo_fornecedor on produto = codigo_produto where empresa = ").append(usuario.getDeposito().getCodigo()).append(" and ").append(pesquisa).append(" ilike'%").append(jTextFieldPesquisa.getText()).append("%' order by ").append(pesquisa).append("");
            try {
                Connection conexao;
                conexao = ConexaoBD.conectar();
                PreparedStatement comando = conexao.prepareStatement(sql.toString());
                ResultSet resultado = comando.executeQuery();
                jList1.removeAll();
                Modelo.removeAllElements();
                while (resultado.next()) {
                    Modelo.addElement(resultado.getString(pesquisa));
                }
                conexao.close();
                int i = jList1.getModel().getSize();
                if (i == 0) {
                    jList1.setVisible(false);
                } else {
                    jList1.setVisible(true);
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, ex);
            }
        }
    }

