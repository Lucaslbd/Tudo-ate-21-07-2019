    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (selecionado == 0) {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma baixa para poder excluir");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Ao remover uma baixa também serão removidos seus respectivos registros");
            int resposta;
            resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente excluir a baixa?");
            if (resposta == JOptionPane.YES_OPTION) {
                BaixaDao dao = new BaixaDao();
                dao.excluir(selecionado);
            }
            selecionado = 0;
            Pesquisar();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (!pesquisou) {
            JOptionPane.showMessageDialog(rootPane, "Faça um pesquisa para poder somar");
        } else {
            UtilitarioDao util = new UtilitarioDao();
            double count = 0;
            for (int i = 0; i <= jTablePadrao.getRowCount() - 1; i++) {
                count += Double.parseDouble(jTablePadrao.getValueAt(i, 6).toString());
            }
            JOptionPane.showMessageDialog(null, "O valor total em baixas é  " + util.DecimalFormat(count));
        }
    }//GEN-LAST:event_jButton4ActionPerformed

