    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextFieldNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "O campo nome não pode estar vazio");
            jTextFieldNome.requestFocus();       
        } else if (jTextFieldCpf.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "O campo cpf não pode estar vazio");
            jTextFieldCpf.requestFocus();
        } else if (jPasswordFieldSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "O campo senha não pode estar vazio");
            jPasswordFieldSenha.requestFocus();        
        } else {
            Usuario c = new Usuario();           
            c.setCpf(jTextFieldCpf.getText());            
            c.setNome(jTextFieldNome.getText());           
            c.setTipo((String) jComboBox1.getSelectedItem());
            c.setSenha(jPasswordFieldSenha.getText());           
            c.setCodigo(usuario.getCodigo());
            c.setDeposito(usuario.getDeposito());
            dao.Alterar(c);
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

