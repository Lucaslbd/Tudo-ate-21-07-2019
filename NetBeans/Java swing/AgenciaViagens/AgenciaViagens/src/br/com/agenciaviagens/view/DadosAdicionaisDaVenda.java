    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jDateChooserCheckin.getDate() == null) {
            JOptionPane.showMessageDialog(rootPane, "Preencha o campo data");
        } else if (jFormattedTextFieldHora.getText().trim().length() < 5) {
            JOptionPane.showMessageDialog(rootPane, "Preencha o campo hora");
            jFormattedTextFieldHora.requestFocus();
        } else {
            CheckinDao dao = new CheckinDao();
            Checkin check = new Checkin();
            check.setDataCheckin(jDateChooserCheckin.getDate());
            check.setHoraCheckin(jFormattedTextFieldHora.getText());
            check.setVenda(venda);
            dao.Salvar(check, empresa);
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

