    private void jButton1MouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseEntered
        try {
            Double.parseDouble(jTextFieldValor.getText());
        } catch (NumberFormatException e) {
            if (!"".equals(jTextFieldValor.getText())) {
                JOptionPane.showMessageDialog(null, "O campo valor somente pode receber numeros reais ex: 50.50");
                jTextFieldValor.setText("");
                jTextFieldValor.requestFocus();
            }
        }
    }//GEN-LAST:event_jButton1MouseEntered

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextFieldDescricao.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Prencha o campo descricão");
            jTextFieldDescricao.requestFocus();
        } else if (jDateChooser1.getDate() == null) {
            JOptionPane.showMessageDialog(rootPane, "Prencha o campo data");
        } else if (jTextFieldValor.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Prencha o campo valor");
            jTextFieldValor.requestFocus();
        } else {
            Gastos gasto = new Gastos();
            GastosDao dao = new GastosDao();
            gasto.setDescricao(jTextFieldDescricao.getText());
            gasto.setValor(Double.parseDouble(jTextFieldValor.getText()));
            gasto.setData(jDateChooser1.getDate());
            gasto.setCodigo(codigo);
            dao.Alterar(gasto, empresa);
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

