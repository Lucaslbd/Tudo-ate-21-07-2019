    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o campo token");
        } else {
            StringBuilder sql = new StringBuilder();
            sql.append("update token set token=? where codigo=?");
            try (Connection conexao = ConexaoBD.conectar(empresa.getCaminho(), empresa.getBanco(), empresa.getSenhaBanco())) {
                PreparedStatement comando = conexao.prepareStatement(sql.toString());
                comando.setString(1, jTextField1.getText());
                comando.setInt(2, 1);
                comando.executeUpdate();
                conexao.close();
                JOptionPane.showMessageDialog(null, "Token alterado com sucesso");
                preencherTabela("select * from token");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Erro ao alterar token" + ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void preencherTabela(String sql) {
        String[] colunas = new String[]{"Token"};
        ArrayList dados = new ArrayList();
        try {
            Connection conexao;
            conexao = ConexaoBD.conectar(empresa.getCaminho(), empresa.getBanco(), empresa.getSenhaBanco());
            PreparedStatement comando = conexao.prepareStatement(sql);
            ResultSet resultado = comando.executeQuery();
            while (resultado.next()) {
                dados.add(new Object[]{resultado.getString("token")});
            }
            conexao.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jTablePadrao.setModel(modelo);
        jTablePadrao.getColumnModel().getColumn(0).setPreferredWidth(500);
        jTablePadrao.getColumnModel().getColumn(0).setResizable(false);
        jTablePadrao.getTableHeader().setReorderingAllowed(false);
        jTablePadrao.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        jTablePadrao.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

    }

