    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Thread t = new Thread() {
            @Override
            public void run() {
                if (jPasswordField1.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(rootPane, "Preencha o campo senha");
                    jPasswordField1.requestFocus();
                } else if (jTextField1.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(rootPane, "Preencha o campo assunto");
                    jTextField1.requestFocus();
                } else if (jTextArea1.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(rootPane, "Preencha o campo mensagem");
                    jTextArea1.requestFocus();
                } else {
                    dialog.setVisible(true);
                    for (String mail : listaEmail) {
                        try {
                            SimpleEmail email = new SimpleEmail();
                            email.setHostName("smtp.gmail.com"); // o servidor SMTP para envio do e-mail smtp.googlemail.com
                            email.addTo(mail); //destinat√°rio
                            email.setFrom(jLabelEmail.getText()); // remetente
                            email.setSubject(jTextField1.getText());  // assunto do e-mail
                            email.setMsg(jTextArea1.getText());       //conteudo do e-mail
                            email.setAuthentication(jLabelEmail.getText(), jPasswordField1.getText());
                            email.setSmtpPort(465);  //465 ou 587
                            email.setSSLOnConnect(true);
                            email.send();
                        } catch (EmailException ex) {
                            JOptionPane.showMessageDialog(rootPane, "Erro ao enviar email para (" + mail + ") Talvez o e-mail n√£o exista\n" + ex);
                        }
                    }
                    JOptionPane.showMessageDialog(rootPane, "Processo finalizado com sucesso");
                    dispose();
                }
                dialog.dispose();
            }
        };
        t.start();
    }//GEN-LAST:event_jButton1ActionPerformed

